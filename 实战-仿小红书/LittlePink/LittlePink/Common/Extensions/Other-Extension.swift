import Foundation
import FaveButton

extension FaveButton {
    
    func setToNormal() {
        self.selectedColor = self.normalColor
        self.dotFirstColor = self.normalColor
        self.dotSecondColor = self.normalColor
        self.circleFromColor = self.normalColor
        self.circleToColor = self.normalColor
    }
    
}
